# set - data collection with sets
# indexing - THERE IS NO INDEXING IN SETS
# sets are unordered
# Syntax: {} brackets for sets

car_parts = {"wheels", "doors", "radio"}
print(car_parts)

# managing data with sets
# add an item to the sets
car_parts.add("seat belts")
print(car_parts)
# remove an item from the set
car_parts.remove("wheels")
print(car_parts)